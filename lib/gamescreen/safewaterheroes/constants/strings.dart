  static String get(String key, String langCode) {
    return _localizedValues[langCode]?[key] ?? _localizedValues['en']![key] ?? key;
  }

  /// Translates English text to Spanish for audio playback.
  /// If targetLang is English, returns text unchanged.
  /// If targetLang is Spanish, looks up translation; if not found,
  /// checks if text is already a Spanish value (pre-resolved by caller)
  /// and returns it as-is rather than speaking English with Spanish voice.
  static String translateForAudio(String text, String targetLang) {
    // If language is English, always return original text
    if (targetLang != 'es') return text;

    // Direct match (English key → Spanish value)
    if (_audioTranslations.containsKey(text)) {
      return _audioTranslations[text]!;
    }

    // Case-insensitive match
    final lowerText = text.toLowerCase();
    for (var entry in _audioTranslations.entries) {
      if (entry.key.toLowerCase() == lowerText) {
        return entry.value;
      }
    }

    // Check if text is already a Spanish value (pre-resolved by caller)
    // If so, return as-is — it's already in the target language
    for (var value in _audioTranslations.values) {
      if (value == text || value.toLowerCase() == lowerText) {
        return text;
      }
    }

    // Return original text — may already be in target language
    return text;
  }
